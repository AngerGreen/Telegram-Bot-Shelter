CREATE TABLE IF NOT EXISTS pet
(
    id                     BIGINT generated by default as identity primary key,
    pet_type               TEXT      NOT NULL,
    chat_id                BIGINT    NOT NULL REFERENCES users (user_chat_id),
    pet_name               TEXT      NOT NULL,
    available              BOOL      NOT NULL
);

CREATE TABLE IF NOT EXISTS client
(
    id                     BIGINT generated by default as identity primary key,
    name_client            TEXT      NOT NULL,
    phoneNumber            INT       NOT NULL,
    address                TEXT      NOT NULL,
    pet_id                 BIGINT    NOT NULL REFERENCES pet (id)
);

CREATE TABLE IF NOT EXISTS shelter
(
    id                     BIGINT generated by default as identity primary key,
    name_shelter           TEXT      NOT NULL,
    address                TEXT      NOT NULL,
    schedule               INT       NOT NULL,
    about                  TEXT      NOT NULL,
    guard                  TEXT      NOT NULL,
    location_map           TEXT      NOT NULL,
    pet_type               TEXT      NOT NULL,
    volunteer_set          BIGINT    NOT NULL REFERENCES shelter (id),
    user_set               BIGINT    NOT NULL REFERENCES users (id)
);

CREATE TABLE IF NOT EXISTS users
(
    id                     BIGINT generated by default as identity primary key,
    name_user              TEXT      NOT NULL,
    phoneNumber            INT       NOT NULL,
    shelter_id             INT       NOT NULL REFERENCES shelter (id),
    user_chat_id           BIGINT    NOT NULL
);

CREATE TABLE IF NOT EXISTS volunteers
(
    id                     BIGINT generated by default as identity primary key,
    name_volunteer         TEXT      NOT NULL,
    chat_id                BIGINT    NOT NULL REFERENCES users (user_chat_id),
    available              BOOL      NOT NULL

);

CREATE TABLE IF NOT EXISTS report
(
    id                     BIGINT generated by default as identity primary key,
    chat_id                BIGINT    NOT NULL REFERENCES users (user_chat_id),
    pet_name               TEXT      NOT NULL,
    health                 TEXT      NOT NULL,
    ration                 TEXT      NOT NULL,
    behaviour              TEXT      NOT NULL,
    message_date           DATE      NOT NULL,
    file_img               VARCHAR   NOT NULL
);





